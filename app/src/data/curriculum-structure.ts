/** الحصول على جميع الدروس مسطّحة */
export function getAllLessons(): Lesson[] {
  return curriculumStructure.units.flatMap((unit) => unit.lessons);
}

/** الحصول على درس بالرقم */
export function getLessonByNumber(lessonNumber: string): Lesson | undefined {
  return getAllLessons().find((lesson) => lesson.number === lessonNumber);
}

/** الحصول على وحدة بالرقم */
export function getUnitByNumber(unitNumber: number): Unit | undefined {
  return curriculumStructure.units.find((unit) => unit.number === unitNumber);
}

/** الحصول على جميع نتاجات التعلم لوحدة */
export function getUnitLearningOutcomes(unitNumber: number): string[] {
  const unit = getUnitByNumber(unitNumber);
  if (!unit) return [];
  return unit.lessons.flatMap((lesson) => lesson.learning_outcomes);
}

/** الحصول على جميع المصطلحات الجديدة */
export function getAllNewTerms(): BilingualTerm[] {
  return getAllLessons().flatMap((lesson) => lesson.new_terms);
}

/** الحصول على عدد الدروس الإجمالي */
export function getTotalLessonCount(): number {
  return getAllLessons().length;
}

